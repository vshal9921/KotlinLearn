package basics

fun main(){

    // Post Increment
    // First uses the value and the increments it
    var i = 11
    println(i++)

    //Post Increment
    // First increments the value and the uses it
    var j = 15
    println(++j)

    // Task- calculate output
    var a = 10

    println(a++ + ++a + a++)   // 10 + 12 + 12 = 34
}